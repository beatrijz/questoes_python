raio = float (input('Digite o valor do raio do circulo: '))
PI= 3.14
area = PI * (raio ** 2)
perimetro = 2 * PI * raio
diametro = 2 * raio
print (f"O valor da área do circulo é: {area :.2f}")
print (f"O perímetro da circunferência é: {perimetro :.2f}")
print (f"O diâmetro da circunferência é: {diametro :.2f}")
