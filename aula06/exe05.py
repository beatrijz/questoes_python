num1 = int (input ('Digite o primeiro número: '))
num2 = int (input ('Digite o segundo número: '))
num3 = float (input('Digite o terceiro número: '))

funcao1 = (num1 * 2) * (num2 / 2)
funcao2 = (num1 * 3) + num3
funcao3 = num3 ** 3

print (f'O resultado do produto do dobro do primeiro com metade do segundo é {funcao1}')
print (f'A soma do triplo do primeiro com o terceiro é {funcao2}')
print (f'O terceiro elevedo ao cubo é {funcao3}')