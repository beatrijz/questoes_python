idade = int (input ('Digite a idade: '))
if idade >= 5 and idade <= 7:
    print ('A categoria do nadador é a infantil A.')

if idade >= 8 and idade <= 10:
    print ('A categoria do nadador é a infantil B.')

if idade >= 11 and idade <= 13:
    print ('A categoria do nadador é a de juvenil A.')

if idade >= 14 and idade <= 17:
    print ('A categoria do nadador é a juvenil B.')

if idade >= 18:
    print ('A categoria do nadador é a de adulto.')
if idade <= 4:
    print ('Idade inválida.')

